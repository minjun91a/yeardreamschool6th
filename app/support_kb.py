def support_transcript(messages) -> str:
    """Gemini 단발 호출용으로 대화 기록을 사람이 읽는 형태로 펼친다(멀티턴 맥락 유지)."""
    lines = []
    for m in (messages or []):
        content = (m.get("content") or "").strip()
        if not content:
            continue
        who = "사용자" if m.get("role") == "user" else "상담원"
        lines.append(f"{who}: {content}")
    lines.append("상담원:")
    return "\n".join(lines)
